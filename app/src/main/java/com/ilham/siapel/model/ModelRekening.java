package com.ilham.siapel.model;

public class ModelRekening {
    private String id_rek;
    private String nama_rek;

    public ModelRekening() {
        this.id_rek = id_rek;
        this.nama_rek = nama_rek;
    }

    public String getId_rek() {
        return id_rek;
    }

    public void setId_rek(String id_rek) {
        this.id_rek = id_rek;
    }

    public String getNama_rek() {
        return nama_rek;
    }

    public void setNama_rek(String nama_rek) {
        this.nama_rek = nama_rek;
    }
}
